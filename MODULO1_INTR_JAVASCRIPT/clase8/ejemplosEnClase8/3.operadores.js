console.log (a - b);
console.log (a * b);
console.log (a / b);
console.log (a % b);


//Operadores de comparacion
console.log (a < b);
console.log (a > b);
console.log (a === b);

//Operadores logicos
console.log (true && false);
console.log (true || false);
console.log (!true);
